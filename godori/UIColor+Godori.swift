import Foundation
import UIKit

extension UIColor {
    public class var godoriPink: UIColor {
        return UIColor(red: 233/255, green: 30/255, blue: 99/255, alpha: 1.0)
    }
    
    public class var godoriPurple: UIColor {
        return UIColor(red: 63/255, green: 81/255, blue: 181/255, alpha: 1.0)
    }
}
